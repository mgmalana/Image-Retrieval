public class LUV {
    private double l;
    private double u;
    private double v;

    public LUV(double l, double u, double v) {
        this.l = l;
        this.u = u;
        this.v = v;
    }

    public double getL() {
        return l;
    }

    public double getU() {
        return u;
    }

    public double getV() {
        return v;
    }
}
